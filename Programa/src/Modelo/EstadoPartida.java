package Modelo;

/**
 * Enum que representa los posibles estados de una partida.
 *
 * @author JP-Aceves
 * @version 1.0
 */
public enum EstadoPartida {
    /** La partida está en curso. */
    EN_CURSO,
    /** La partida está pausada. */
    PAUSADA,
    /** La partida ha finalizado. */
    FINALIZADA
}

